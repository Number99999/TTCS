import model.*;

public class product {

    private int id, price;
    private String link_img, content, name, categories, chiTiet, tuKhoa;     //chưa kết nối giữa loại hàng và sản phẩm

    public product(int id, String link_img, String name, String content, String categories, String chiTiet, String tuKhoa, int price) {
        this.id = id;
        this.link_img = link_img;
        this.name = name;
        this.content = content;
        this.categories = categories;
        this.chiTiet = chiTiet;
        this.tuKhoa = tuKhoa;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getLink_img() {
        return link_img;
    }

    public void setLink_img(String link_img) {
        this.link_img = link_img;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCategories() {
        return categories;
    }

    public void setCategories(String categories) {
        this.categories = categories;
    }

    public String getChiTiet() {
        return chiTiet;
    }

    public void setChiTiet(String chiTiet) {
        this.chiTiet = chiTiet;
    }

    public String getTuKhoa() {
        return tuKhoa;
    }

    public void setTuKhoa(String tuKhoa) {
        this.tuKhoa = tuKhoa;
    }

}
